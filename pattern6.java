class pattern6
{
    public static void main(String args[])
    {
        int m=1;
        for(int i = 1; i<=10;i++)
        {
            for(int j = 1; j<=10;j++)
            {
                System.out.print(m + " ");
                m++;
            }
            System.out.println();
        }
    }
}